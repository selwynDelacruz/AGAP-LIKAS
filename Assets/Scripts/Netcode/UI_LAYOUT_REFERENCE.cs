 * ============================================================================
 * RESPONSIVE BEHAVIOR
 * ============================================================================
 * 
 * When Host button clicked:
 *   ConnectionPanel ? SetActive(false)
 *   LobbyInfoPanel ? SetActive(true)
 *   HostControlsPanel ? SetActive(true)
 *   DisconnectButton ? SetActive(true)
 * 
 * When Client button clicked:
 *   ConnectionPanel ? SetActive(false)
 *   LobbyInfoPanel ? SetActive(true)
 *   HostControlsPanel ? SetActive(false) // Clients don't see host controls
 *   DisconnectButton ? SetActive(true)
 * 
 * When Disconnect button clicked:
 *   ConnectionPanel ? SetActive(true)
 *   LobbyInfoPanel ? SetActive(false)
 *   HostControlsPanel ? SetActive(false)
 *   DisconnectButton ? SetActive(false)
 * 
 * ============================================================================
 * CANVAS SETTINGS RECOMMENDATION
 * ============================================================================
 * 
 * Canvas:
 * - Render Mode: Screen Space - Overlay
 * - UI Scale Mode: Scale With Screen Size
 * - Reference Resolution: 1920 x 1080
 * - Match: 0.5 (balance width/height)
 * 
 * Canvas Scaler:
 * - Ensures UI looks good on different screen sizes
 * 
 * Graphic Raycaster:
 * - Required for button clicks to work
 * 
 * ============================================================================
 * LAYOUT TIPS
 * ============================================================================
 * 
 * 1. Use Anchors:
 *    - Set anchors to center/middle for main panels
 *    - This ensures UI stays centered on different resolutions
 * 
 * 2. Use Layout Groups:
 *    - Vertical Layout Group for stacked elements
 *    - Horizontal Layout Group for side-by-side buttons
 *    - Grid Layout Group for organized button arrays
 * 
 * 3. Use Content Size Fitter:
 *    - On text elements to auto-size based on content
 *    - Helps prevent text cutoff
 * 
 * 4. Use Padding:
 *    - Add spacing in Layout Groups (10-20 pixels)
 *    - Makes UI more readable
